template <typename T, typename V>
auto print_cycles(T& t, V& v, unsigned line) -> void
{
    cout << "\n---" << line << "---" << '\n';
    for(auto e : t)
        std::cout << std::distance(v.begin(), e.first) << ":" << std::distance(v.begin(), e.second) << std::endl;
    cout << "---\n";
}

